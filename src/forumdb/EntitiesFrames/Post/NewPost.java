    public NewPost(java.awt.Frame parent, boolean modal, Connection c) {
        super(parent, modal);
        initComponents();
        ((AbstractDocument) text.getDocument()).setDocumentFilter(new JTextFieldLimit(1024));
        this.c = c;
        list = new ArrayList<>();
        list1 = new ArrayList<>();

        try {
            Statement statement = c.createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM \"Section\"");
            while (rs.next()) {
                Section item = new Section(rs.getInt("ID_sect"), rs.getString("Name_sect"));
                list.add(item);
            }
            section.setModel(new DefaultComboBoxModel(list.toArray()));

            rs = statement.executeQuery("SELECT * FROM \"Customer\"");
            while (rs.next()) {
                Customer item = new Customer(rs.getInt("ID_cust"), rs.getString("Login"), rs.getString("Password"),
                        rs.getInt("ID_role"), rs.getString("Family"),
                        rs.getString("Name"), rs.getString("Patronymic"), rs.getString("Email"));
                list1.add(item);
            }
            user.setModel(new DefaultComboBoxModel(list1.toArray()));

        } catch (SQLException ex) {
            Logger.getLogger(NewCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

