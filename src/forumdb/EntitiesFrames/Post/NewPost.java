    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!check()) {
            return;
        }
        try {
            // TODO add your handling code here:
            Statement statement = c.createStatement();
            if (editItem == null) {
                statement.executeUpdate("insert into \"Post\" (\"ID_cust\", \"ID_sect\", \"Text\")"
                        + " values ('" + ((Customer) user.getSelectedItem()).getID_cust() + "','" + ((Section) section.getSelectedItem()).getID_sect() + "','" + text.getText() + "');");
            } else {
                statement.executeUpdate("update \"Post\" set \"ID_cust\" = '" + ((Customer) user.getSelectedItem()).getID_cust() + "', \"ID_sect\" ='" + ((Section) section.getSelectedItem()).getID_sect() + "',\"Text\"='" + text.getText() + "' where \"ID_post\" = " + editItem.getID_post() + ";");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage());
            return;
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

