    private void fillFields() {
        text.setText(editItem.getText());
        for (Section s : list) {
            if (s.getID_sect() == editItem.getID_sect()) {
                section.setSelectedItem((s));
            }
        }
        for (Customer s : list1) {
            if (s.getID_cust() == editItem.getID_cust()) {
                user.setSelectedItem((s));
            }
        }
    }

    public boolean check() {
        if ("".equals(text.getText())) {
            JOptionPane.showMessageDialog(new JFrame(), "Name cannot be empty");
            return false;
        }
        return true;
    }

